#!/usr/bin/env python3
"""benchgen.py — Generate benchmark .cu files from @bench annotations in kernel headers.

Scans src/csrc/kernels/*.cuh for // @bench annotation blocks above dispatch functions,
then generates complete benchmark files into src/csrc/benchmarks/.

Usage:
    python3 scripts/benchgen.py           # Generate/overwrite benchmark files
    python3 scripts/benchgen.py --check   # Check if generated files are up-to-date
    python3 scripts/benchgen.py --dry-run # Print generated code without writing
"""

import argparse
import os
import re
import sys
import tempfile
from dataclasses import dataclass, field
from pathlib import Path

REPO_ROOT = Path(__file__).resolve().parent.parent
KERNELS_DIR = REPO_ROOT / "src" / "csrc" / "kernels"
BENCH_DIR = REPO_ROOT / "src" / "csrc" / "benchmarks"
GENERATED_HEADER = "// AUTO-GENERATED by benchgen.py — do not edit manually"


@dataclass
class Buffer:
    name: str
    type: str  # "float" or "floatX"
    size_expr: str  # C++ expression e.g. "N*C"
    fill: str = (
        ""  # "random(lo,hi)", "bf16(lo,hi)", "zero", "ones(val)", "computed", ""
    )
    is_input: bool = True


@dataclass
class ProfileSpec:
    func_name: str
    config_vars: dict  # e.g. {"N": "1024", "C": "4096"}
    max_entries: str = "8"
    grid: str = ""  # expression for num_blocks in profile_kernel
    timeline: bool = True
    timeline_blocks: str = "1"


@dataclass
class BenchSpec:
    name: str  # benchmark name (becomes bench_<name>.cu if no group)
    group: str  # group name — grouped specs share a file
    func_name: str  # dispatch function name
    source_file: str  # relative path to source .cuh
    configs_dims: list  # e.g. ["N", "C"] or ["M", "N", "num_iters"]
    configs_values: list  # e.g. [(128,4096), (256,4096), ...]
    buffers: list  # list of Buffer
    scalars: dict  # e.g. {"eps": "1e-5f"}
    flops_expr: str = ""  # C++ expression for FLOPs
    bandwidth_expr: str = ""  # C++ expression for bytes (default: auto-computed)
    profile: ProfileSpec | None = None
    runs: int = 100
    pre_iter: str = ""  # per-iteration setup e.g. "d_d_weight.zero()"
    pre_setup_lines: list = field(
        default_factory=list
    )  # code before device allocs (e.g. workspace calc)
    setup_lines: list = field(
        default_factory=list
    )  # code after device allocs (e.g. host compute + upload)
    call_args: list = field(default_factory=list)  # ordered arg names for function call
    call_expr: str = (
        ""  # override for the bench_kernel call (e.g. "fused.forward(...)")
    )
    cleanup_lines: list = field(
        default_factory=list
    )  # code after bench loop (e.g. "fused.destroy()")
    tflops_expr: str = ""  # FLOPs expression for TFLOPS metric (separate from GFLOPS)
    metrics: list = field(default_factory=list)  # custom metrics: [(name, expr), ...]
    title: str = ""  # section title override


def parse_annotation_block(lines: list[str]) -> dict[str, list[str]]:
    """Parse a block of // @key: value lines into a dict.

    Continuation lines (starting with // but not // @) are appended to the
    last value of the current key, allowing long annotations to wrap.
    """
    result = {}
    current_key = None
    for line in lines:
        line = line.strip()
        if line.startswith("// @"):
            line = line[4:]  # strip "// @"
            # Check for key: value
            m = re.match(r"(\S+?):\s?(.*)", line)
            if m:
                current_key = m.group(1)
                val = m.group(2)
                # Strip for non-setup keys (setup/pre-setup/cleanup preserve indentation)
                if current_key in ("setup", "pre-setup", "cleanup"):
                    val = val.rstrip()
                else:
                    val = val.strip()
                if current_key not in result:
                    result[current_key] = []
                if val:
                    result[current_key].append(val)
            else:
                # Key without colon — e.g. "@bench" or "@bench custom_name"
                parts = line.split(None, 1)
                current_key = parts[0]
                val = parts[1].strip() if len(parts) > 1 else ""
                if current_key not in result:
                    result[current_key] = []
                if val:
                    result[current_key].append(val)
        elif line.startswith("//") and current_key is not None:
            # Continuation line — append to the last value of current key
            cont = line[2:].strip()
            if cont:
                if current_key in result and result[current_key]:
                    result[current_key][-1] += " " + cont
                else:
                    if current_key not in result:
                        result[current_key] = []
                    result[current_key].append(cont)
    return result


def parse_configs(config_str: str) -> tuple[list[str], list[tuple]]:
    """Parse config string like '(N,C) = [(128,4096),(256,4096)]' into dims and values."""
    m = re.match(r"\(([^)]+)\)\s*=\s*\[(.+)\]", config_str)
    if not m:
        raise ValueError(f"Invalid @configs format: {config_str}")
    dims = [d.strip() for d in m.group(1).split(",")]
    values_str = m.group(2)
    values = []
    for tup in re.findall(r"\(([^)]+)\)", values_str):
        vals = tuple(v.strip() for v in tup.split(","))
        values.append(vals)
    return dims, values


def parse_buffer(buf_str: str, is_input: bool) -> Buffer:
    """Parse buffer spec like 'inp float[N*C] random(-1,1)' or 'out float[N*C]'."""
    # Pattern: name type[size] [fill]
    m = re.match(r"(\w+)\s+(\w+)\[([^\]]+)\](?:\s+(.+))?", buf_str.strip())
    if not m:
        raise ValueError(f"Invalid buffer spec: {buf_str}")
    name = m.group(1)
    typ = m.group(2)
    size = m.group(3)
    fill_str = m.group(4).strip() if m.group(4) else ""
    return Buffer(name=name, type=typ, size_expr=size, fill=fill_str, is_input=is_input)


def parse_profile(profile_str: str) -> ProfileSpec:
    """Parse profile spec like 'rmsnorm_forward_profiled N=1024 C=4096 max_entries=8 grid=N'."""
    parts = profile_str.split()
    func_name = parts[0]
    config_vars = {}
    max_entries = "8"
    grid = ""
    timeline = False
    timeline_blocks = "1"
    for part in parts[1:]:
        if "=" not in part:
            continue
        k, v = part.split("=", 1)
        if k == "max_entries":
            max_entries = v
        elif k == "grid":
            grid = v
        elif k == "timeline":
            timeline = v.lower() == "true"
        elif k == "timeline_blocks":
            timeline_blocks = v
        else:
            config_vars[k] = v
    return ProfileSpec(
        func_name=func_name,
        config_vars=config_vars,
        max_entries=max_entries,
        grid=grid,
        timeline=timeline,
        timeline_blocks=timeline_blocks,
    )


def parse_func_signature(sig_lines: str) -> tuple[str, list[tuple[str, str]]]:
    """Parse function signature to extract name and (type, name) params."""
    sig = " ".join(sig_lines.split())
    # Extract function name
    m = re.search(r"(\w+)\s*\(", sig)
    if not m:
        raise ValueError(f"Cannot find function name in: {sig}")
    func_name = m.group(1)
    # Extract params
    paren_start = sig.index("(")
    paren_end = sig.rindex(")")
    params_str = sig[paren_start + 1 : paren_end]
    params = []
    for p in split_params(params_str):
        p = p.strip()
        if not p:
            continue
        # Remove default value
        if "=" in p:
            p = p[: p.index("=")].strip()
        # Split type and name
        parts = p.rsplit(None, 1)
        if len(parts) == 2:
            params.append((parts[0].strip(), parts[1].strip()))
    return func_name, params


def split_params(s: str) -> list[str]:
    """Split comma-separated params, respecting nested angle brackets and parens."""
    depth = 0
    parts = []
    current = []
    for c in s:
        if c in "(<":
            depth += 1
        elif c in ")>":
            depth -= 1
        elif c == "," and depth == 0:
            parts.append("".join(current))
            current = []
            continue
        current.append(c)
    if current:
        parts.append("".join(current))
    return parts


def scan_kernel_file(filepath: Path) -> list[BenchSpec]:
    """Scan a .cuh file for @bench annotation blocks and return BenchSpecs."""
    lines = filepath.read_text().splitlines()
    specs = []

    i = 0
    while i < len(lines):
        line = lines[i].strip()
        if not line.startswith("// @bench"):
            i += 1
            continue

        # Found a @bench block — collect all contiguous // @ lines and // continuation lines
        block_start = i
        block_lines = []
        while i < len(lines):
            stripped = lines[i].strip()
            if stripped.startswith("// @") or (
                stripped.startswith("//")
                and not stripped.startswith("///")
                and block_lines
            ):
                block_lines.append(lines[i])
                i += 1
            else:
                break

        # Parse annotation block first to check for @call
        ann = parse_annotation_block(block_lines)

        # If @call is present, function signature is optional
        call_expr = " ".join(ann.get("call", []))
        if call_expr:
            # No function signature to parse — don't advance i
            func_name = ""
            params = []
        else:
            # Skip blank lines between annotation and function
            while i < len(lines) and not lines[i].strip():
                i += 1

            # Collect function signature (until opening brace)
            sig_lines = []
            while i < len(lines):
                sig_lines.append(lines[i])
                if "{" in lines[i]:
                    break
                i += 1

            sig_text = " ".join(l.strip() for l in sig_lines)
            # Remove the { and everything after
            sig_text = sig_text[: sig_text.index("{")]

            func_name, params = parse_func_signature(sig_text)

        # Build BenchSpec
        bench_name = ann.get("bench", [""])[0] or func_name
        group = ann.get("group", [""])[0] if "group" in ann else ""

        # Parse configs
        config_str = " ".join(ann.get("configs", []))
        dims, values = parse_configs(config_str)

        # Parse buffers
        buffers = []
        for buf_str in ann.get("in", []):
            # Multiple buffers can be comma-separated
            for b in split_buf_list(buf_str):
                buffers.append(parse_buffer(b, is_input=True))
        for buf_str in ann.get("out", []):
            for b in split_buf_list(buf_str):
                buffers.append(parse_buffer(b, is_input=False))
        for buf_str in ann.get("extra-buf", []):
            for b in split_buf_list(buf_str):
                buffers.append(parse_buffer(b, is_input=True))

        # Parse scalars
        scalars = {}
        for s in ann.get("scalar", []):
            for pair in s.split(","):
                pair = pair.strip()
                if "=" in pair:
                    k, v = pair.split("=", 1)
                    scalars[k.strip()] = v.strip()

        # Parse profile
        profile = None
        if "profile" in ann:
            profile = parse_profile(" ".join(ann["profile"]))

        # Parse other fields
        flops_expr = " ".join(ann.get("flops", []))
        tflops_expr = " ".join(ann.get("tflops", []))
        bandwidth_expr = " ".join(ann.get("bandwidth", []))
        runs = int(ann.get("runs", ["100"])[0])
        pre_iter = " ".join(ann.get("pre-iter", []))
        pre_setup_lines = ann.get("pre-setup", [])
        setup_lines = ann.get("setup", [])
        cleanup_lines = ann.get("cleanup", [])
        title = " ".join(ann.get("title", []))

        # Parse custom metrics: "name=expression"
        metrics = []
        for m in ann.get("metric", []):
            if "=" in m:
                mname, mexpr = m.split("=", 1)
                metrics.append((mname.strip(), mexpr.strip()))

        # Determine call args from function params
        call_args = []
        for _ptype, pname in params:
            if pname == "stream":
                continue  # skip stream param (uses default nullptr)
            call_args.append(pname)

        rel_path = filepath.relative_to(REPO_ROOT / "src" / "csrc")

        spec = BenchSpec(
            name=bench_name,
            group=group,
            func_name=func_name,
            source_file=str(rel_path),
            configs_dims=dims,
            configs_values=values,
            buffers=buffers,
            scalars=scalars,
            flops_expr=flops_expr,
            tflops_expr=tflops_expr,
            bandwidth_expr=bandwidth_expr,
            profile=profile,
            runs=runs,
            pre_iter=pre_iter,
            pre_setup_lines=pre_setup_lines,
            setup_lines=setup_lines,
            call_args=call_args,
            call_expr=call_expr,
            cleanup_lines=cleanup_lines,
            metrics=metrics,
            title=title,
        )
        specs.append(spec)
        i += 1

    return specs


def split_buf_list(buf_str: str) -> list[str]:
    """Split comma-separated buffer specs, respecting brackets."""
    depth = 0
    parts = []
    current = []
    for c in buf_str:
        if c in "([":
            depth += 1
        elif c in ")]":
            depth -= 1
        elif c == "," and depth == 0:
            parts.append("".join(current).strip())
            current = []
            continue
        current.append(c)
    if current:
        s = "".join(current).strip()
        if s:
            parts.append(s)
    return parts


def float_literal(val: str) -> str:
    """Ensure a value is a valid C++ float literal (e.g. -1 -> -1.0f, 0.75 -> 0.75f)."""
    val = val.strip()
    if val.endswith("f"):
        return val
    # Check if it has a decimal point
    core = val.lstrip("-")
    if "." not in core:
        return val + ".0f"
    return val + "f"


def gen_fill_code(buf: Buffer, host_name: str, size_expr: str) -> list[str]:
    """Generate fill code for a host buffer. Zero fills are handled device-side, not here."""
    code = []
    fill = buf.fill
    if not fill or fill == "computed" or fill == "zero":
        return code
    if fill.startswith("ones("):
        val = fill[5:-1]
        code.append(f"        for (int _i = 0; _i < {size_expr}; _i++)")
        code.append(f"            {host_name}.ptr[_i] = ({buf.type})({val});")
    elif fill.startswith("random("):
        args = fill[7:-1]
        parts = [a.strip() for a in args.split(",")]
        lo, hi = float_literal(parts[0]), float_literal(parts[1])
        seed = parts[2] if len(parts) > 2 else "42"
        if seed == "42":
            code.append(f"        fill_random({host_name}, {size_expr}, {lo}, {hi});")
        else:
            code.append(
                f"        fill_random({host_name}, {size_expr}, {lo}, {hi}, {seed});"
            )
    elif fill.startswith("bf16("):
        args = fill[5:-1]
        parts = [a.strip() for a in args.split(",")]
        lo, hi = float_literal(parts[0]), float_literal(parts[1])
        seed = parts[2] if len(parts) > 2 else "42"
        if seed == "42":
            code.append(
                f"        fill_random_bf16({host_name}, {size_expr}, {lo}, {hi});"
            )
        else:
            code.append(
                f"        fill_random_bf16({host_name}, {size_expr}, {lo}, {hi}, {seed});"
            )
    return code


def compute_bandwidth_expr(buffers: list[Buffer]) -> str:
    """Auto-compute bandwidth expression from buffer sizes."""
    read_parts = []
    write_parts = []
    for buf in buffers:
        sizeof = f"sizeof({buf.type})"
        term = f"(size_t){buf.size_expr} * {sizeof}"
        if buf.is_input:
            read_parts.append(term)
        else:
            write_parts.append(term)
    all_parts = read_parts + write_parts
    if not all_parts:
        return ""
    return " + ".join(all_parts)


def build_call_args(spec: BenchSpec) -> str:
    """Build the function call argument string."""
    args = []
    for arg in spec.call_args:
        # Check if it's a buffer name
        buf = next((b for b in spec.buffers if b.name == arg), None)
        if buf:
            args.append(f"d_{arg}")
        elif arg in spec.scalars:
            args.append(spec.scalars[arg])
        elif arg in spec.configs_dims:
            args.append(arg)
        else:
            # Check if it's a scalar from annotation or a config dim
            args.append(arg)
    return ", ".join(args)


def generate_section(spec: BenchSpec, is_grouped: bool) -> list[str]:
    """Generate a benchmark section for one BenchSpec."""
    code = []
    fn = spec.func_name

    # Section header for grouped benchmarks
    if is_grouped:
        title = spec.title or fn
        code.append(f'    printf("\\n{title}\\n");')

    # Determine which columns to print
    dims = spec.configs_dims
    has_flops = bool(spec.flops_expr)
    has_tflops = bool(spec.tflops_expr)
    has_per_iter = "num_iters" in dims or "iters" in dims
    has_bw = bool(spec.bandwidth_expr) or bool(spec.buffers)

    # Compute column widths and format strings
    col_widths = []
    fmt_parts_v = []
    header_args_h = []

    for d in dims:
        w = 8 if d not in ("iters", "num_iters") else 6
        col_widths.append(w)
        fmt_parts_v.append(f"%{w}d")
        header_args_h.append(f'"{d}"')

    col_widths.append(12)
    fmt_parts_v.append("%12.2f")
    header_args_h.append('"Time (us)"')

    if has_per_iter:
        col_widths.append(10)
        fmt_parts_v.append("%10.2f")
        header_args_h.append('"us/iter"')

    if has_tflops:
        col_widths.append(12)
        fmt_parts_v.append("%12.2f")
        header_args_h.append('"TFLOPS"')
    elif has_flops:
        col_widths.append(10)
        fmt_parts_v.append("%10.2f")
        header_args_h.append('"GFLOPS"')

    if has_bw:
        col_widths.append(12)
        fmt_parts_v.append("%12.2f" if (has_flops or has_tflops) else "%12.0f")
        header_args_h.append('"Bandwidth (GB/s)"')

    for mname, _mexpr in spec.metrics:
        col_widths.append(14)
        fmt_parts_v.append("%14.0f")
        header_args_h.append(f'"{mname}"')

    # Build format strings (space-separated)
    fmt_str_h = " ".join(f"%{w}s" for w in col_widths)
    fmt_str_v = " ".join(fmt_parts_v)

    code.append(f'    printf("{fmt_str_h}\\n", {", ".join(header_args_h)});')
    sep_len = sum(col_widths) + len(col_widths) - 1
    code.append(f'    printf("{"-" * sep_len}\\n");')
    code.append("")

    # Config loop
    code.append("    for (int c = 0; c < num_configs; c++) {")
    for i, d in enumerate(dims):
        code.append(f"        int {d} = configs[c][{i}];")
    code.append("")

    # Categorize buffers
    input_bufs = [b for b in spec.buffers if b.is_input]
    output_bufs = [b for b in spec.buffers if not b.is_input]

    # Host allocations (for any input buffer that needs host data)
    for buf in input_bufs:
        if buf.fill and buf.fill != "zero":
            code.append(f"        HostMem<{buf.type}> h_{buf.name}({buf.size_expr});")

    # Host fills (auto-generated — computed buffers are filled by @setup)
    for buf in input_bufs:
        fill_lines = gen_fill_code(buf, f"h_{buf.name}", buf.size_expr)
        code.extend(fill_lines)

    if any(b.fill and b.fill not in ("zero", "computed", "") for b in input_bufs):
        code.append("")

    # Pre-setup code (before device allocs — e.g. workspace size calculation)
    if spec.pre_setup_lines:
        for line in spec.pre_setup_lines:
            code.append(f"        {line}")
        code.append("")

    # Device allocations
    for buf in input_bufs:
        code.append(f"        DeviceMem<{buf.type}> d_{buf.name}({buf.size_expr});")
    for buf in output_bufs:
        code.append(f"        DeviceMem<{buf.type}> d_{buf.name}({buf.size_expr});")

    # Uploads (non-computed, non-zero)
    for buf in input_bufs:
        if buf.fill == "zero":
            code.append(f"        d_{buf.name}.zero();")
        elif buf.fill and buf.fill != "computed":
            code.append(f"        d_{buf.name}.upload(h_{buf.name});")

    # Post-alloc setup code (after device allocs — e.g. host computation + upload, forward pass)
    if spec.setup_lines:
        code.append("")
        for setup_line in spec.setup_lines:
            code.append(f"        {setup_line}")

    code.append("")

    # Bandwidth computation
    bw_expr = spec.bandwidth_expr or (
        compute_bandwidth_expr(spec.buffers) if spec.buffers else ""
    )
    if bw_expr:
        code.append(f"        size_t total_bytes = {bw_expr};")
        code.append("")

    # Flops computation (declare before bench_kernel for TFLOPS)
    if has_tflops:
        code.append(f"        double flops = {spec.tflops_expr};")
        code.append("")

    # Bench kernel call
    if spec.call_expr:
        call = spec.call_expr
    else:
        call = f"{fn}({build_call_args(spec)})"
    if spec.pre_iter:
        code.append("        float avg_time_ms = bench_kernel(")
        code.append(f"            [&]() {{ {call}; }},")
        code.append(f"            bench_runs, flusher, [&]() {{ {spec.pre_iter}; }});")
    else:
        code.append("        float avg_time_ms = bench_kernel(")
        code.append(f"            [&]() {{ {call}; }},")
        code.append("            bench_runs, flusher);")
    code.append("")

    # Compute metrics
    code.append("        float time_us = avg_time_ms * 1000.0f;")

    iter_dim = None
    for d in ("num_iters", "iters"):
        if d in dims:
            iter_dim = d
            break

    if has_per_iter:
        code.append(f"        float time_per_iter_us = time_us / {iter_dim};")

    if has_tflops:
        code.append("        float tflops = (flops / 1e12f) / (avg_time_ms / 1e3f);")
    elif has_flops:
        code.append(
            f"        float gflops = (({spec.flops_expr}) / 1e9f) / (avg_time_ms / 1e3f);"
        )

    if bw_expr:
        code.append("        float bw = (total_bytes / 1e9f) / (avg_time_ms / 1e3f);")

    for mname, mexpr in spec.metrics:
        varname = mname.lower().replace("/", "_per_").replace(" ", "_")
        code.append(f"        float {varname} = {mexpr};")

    code.append("")

    # Printf values
    val_args = []
    for d in dims:
        val_args.append(d)
    val_args.append("time_us")
    if has_per_iter:
        val_args.append("time_per_iter_us")
    if has_tflops:
        val_args.append("tflops")
    elif has_flops:
        val_args.append("gflops")
    if bw_expr:
        val_args.append("bw")
    for mname, _mexpr in spec.metrics:
        varname = mname.lower().replace("/", "_per_").replace(" ", "_")
        val_args.append(varname)

    code.append(f'        printf("{fmt_str_v}\\n", {", ".join(val_args)});')

    # Cleanup code (per-config, e.g. fused.destroy())
    if spec.cleanup_lines:
        code.append("")
        for cl in spec.cleanup_lines:
            code.append(f"        {cl}")

    code.append("    }")
    code.append("")

    # Profile section
    if spec.profile:
        p = spec.profile
        config_desc = ", ".join(f"{k}={v}" for k, v in p.config_vars.items())
        code.append(f'    printf("\\n--- Phase Breakdown ({config_desc}) ---\\n");')
        code.append("    {")

        # Declare config vars
        for k, v in p.config_vars.items():
            code.append(f"        int {k} = {v};")
        code.append("")

        # Host allocations for profile section
        for buf in input_bufs:
            if buf.fill and buf.fill != "zero":
                code.append(
                    f"        HostMem<{buf.type}> h_{buf.name}({buf.size_expr});"
                )

        for buf in input_bufs:
            fill_lines = gen_fill_code(buf, f"h_{buf.name}", buf.size_expr)
            code.extend(fill_lines)

        if any(b.fill and b.fill not in ("zero", "computed", "") for b in input_bufs):
            code.append("")

        # Pre-setup before device allocs
        if spec.pre_setup_lines:
            for line in spec.pre_setup_lines:
                code.append(f"        {line}")
            code.append("")

        for buf in input_bufs:
            code.append(f"        DeviceMem<{buf.type}> d_{buf.name}({buf.size_expr});")
        for buf in output_bufs:
            code.append(f"        DeviceMem<{buf.type}> d_{buf.name}({buf.size_expr});")

        for buf in input_bufs:
            if buf.fill == "zero":
                code.append(f"        d_{buf.name}.zero();")
            elif buf.fill and buf.fill != "computed":
                code.append(f"        d_{buf.name}.upload(h_{buf.name});")

        # Post-alloc setup for profile section
        if spec.setup_lines:
            code.append("")
            for setup_line in spec.setup_lines:
                code.append(f"        {setup_line}")

        code.append("")

        # Build profile call args
        profile_call_args = build_call_args(spec)
        # Replace function-specific args with profiled variant's args
        # The profiled function has extra profiler_buf, max_entries params
        profile_call = f"{p.func_name}({profile_call_args}, profiler_buf, max_ent)"

        grid = p.grid or "1"
        code.append("        profile_kernel(")
        code.append("            [&](int64_t* profiler_buf, int max_ent) {")
        code.append(f"                {profile_call};")
        code.append("            },")
        if p.timeline:
            code.append(
                f"            {grid}, max_entries, flusher, true, {p.timeline_blocks});"
            )
        else:
            code.append(f"            {grid}, max_entries, flusher);")
        code.append("    }")
    code.append("")

    return code


def generate_benchmark_file(
    specs: list[BenchSpec],
) -> str:
    """Generate a complete benchmark .cu file from one or more BenchSpecs."""
    # Determine source file (use first spec's source for include path)
    source_file = specs[0].source_file
    include_path = f"../{source_file}"

    # File name
    if specs[0].group:
        file_name = specs[0].group
    else:
        file_name = specs[0].name

    lines = []
    lines.append(GENERATED_HEADER)
    lines.append(f"// Source: src/csrc/{source_file}")
    lines.append("// Regenerate: make benchgen")
    lines.append("")
    lines.append("#include <cstdio>")
    lines.append("#include <cuda_runtime.h>")
    lines.append("")
    lines.append(f'#include "{include_path}"')
    lines.append('#include "../include/bench_harness.cuh"')
    lines.append("")
    lines.append("using namespace mhc;")
    lines.append("")
    lines.append("int main() {")
    lines.append(f"    const int bench_runs = {specs[0].runs};")

    # Check if any spec uses scalars
    all_scalars = {}
    for spec in specs:
        all_scalars.update(spec.scalars)
    for k, v in all_scalars.items():
        lines.append(f"    const float {k} = {v};")

    # Check if any spec has profiling
    has_profile = any(s.profile for s in specs)
    if has_profile:
        lines.append("    const int max_entries = 8;")

    lines.append("")
    lines.append("    L2Flusher flusher;")
    lines.append("")

    # Configs — use the first spec's configs (all grouped specs should share)
    dims = specs[0].configs_dims
    values = specs[0].configs_values
    ndims = len(dims)

    lines.append(f"    int configs[][{ndims}] = {{")
    for v in values:
        vals = ", ".join(str(x) for x in v)
        lines.append(f"        {{{vals}}},")
    lines.append("    };")
    lines.append("    int num_configs = sizeof(configs) / sizeof(configs[0]);")
    lines.append("")

    is_grouped = len(specs) > 1

    # Title — for grouped benchmarks, use group name (sections have their own titles)
    if is_grouped:
        title = specs[0].group.replace("_", " ").title()
    else:
        title = specs[0].title or specs[0].name.replace("_", " ").title()
    lines.append(f'    printf("{title} Benchmark\\n");')
    lines.append(f'    printf("{"=" * 70}\\n");')

    for spec in specs:
        section = generate_section(spec, is_grouped)
        lines.extend(section)

    lines.append("    return 0;")
    lines.append("}")
    lines.append("")

    return "\n".join(lines)


def main():
    parser = argparse.ArgumentParser(
        description="Generate benchmark files from annotations"
    )
    parser.add_argument(
        "--check", action="store_true", help="Check if generated files are up-to-date"
    )
    parser.add_argument(
        "--dry-run", action="store_true", help="Print generated code without writing"
    )
    parser.add_argument(
        "--force",
        action="store_true",
        help="Overwrite even if file lacks auto-generated header",
    )
    args = parser.parse_args()

    # Scan all kernel files
    all_specs = []
    for cuh_file in sorted(KERNELS_DIR.glob("*.cuh")):
        specs = scan_kernel_file(cuh_file)
        if specs:
            all_specs.extend(specs)
            print(
                f"  Found {len(specs)} @bench annotation(s) in {cuh_file.name}",
                file=sys.stderr,
            )

    if not all_specs:
        print("No @bench annotations found in kernel files.", file=sys.stderr)
        return

    # Group specs by group name or individual name
    groups = {}
    for spec in all_specs:
        key = spec.group if spec.group else spec.name
        if key not in groups:
            groups[key] = []
        groups[key].append(spec)

    # Generate files
    generated = {}
    for group_name, specs in groups.items():
        content = generate_benchmark_file(specs)
        out_name = f"bench_{group_name}.cu"
        generated[out_name] = content

    if args.dry_run:
        for name, content in sorted(generated.items()):
            print(f"=== {name} ===")
            print(content)
            print()
        return

    if args.check:
        ok = True
        for name, content in sorted(generated.items()):
            out_path = BENCH_DIR / name
            if not out_path.exists():
                print(f"MISSING: {out_path}", file=sys.stderr)
                ok = False
                continue
            existing = out_path.read_text()
            if existing != content:
                print(f"OUT OF DATE: {out_path}", file=sys.stderr)
                ok = False
            else:
                print(f"  OK: {name}", file=sys.stderr)
        if not ok:
            print(
                "\nGenerated benchmarks are out of date. Run 'make benchgen' to update.",
                file=sys.stderr,
            )
            sys.exit(1)
        else:
            print("\nAll generated benchmarks are up to date.", file=sys.stderr)
        return

    # Write files
    for name, content in sorted(generated.items()):
        out_path = BENCH_DIR / name
        # Only overwrite if it's auto-generated, doesn't exist, or --force
        if out_path.exists() and not args.force:
            first_line = out_path.read_text().split("\n", 1)[0]
            if not first_line.startswith(GENERATED_HEADER[:30]):
                print(
                    f"  SKIP: {name} (not auto-generated, use --force to overwrite)",
                    file=sys.stderr,
                )
                continue
        out_path.write_text(content)
        print(f"  Generated: {name}", file=sys.stderr)

    print(f"\nGenerated {len(generated)} benchmark file(s).", file=sys.stderr)


if __name__ == "__main__":
    main()
